using System;
namespace Learnswitchcase{

    class Program
    {
        static void Main(string[] arg)
        {       
            int k=10;
                switch(k)
                {
                    case 3:
                        Console.WriteLine("HowKteam");
                        break;
                    case 9:
                        Console.WriteLine("Kteam");
                        break;
                    case 10:
                        Console.WriteLine("Lambda");
                        break;
                     default:
                        Console.WriteLine("Roots");
                        break;   

                }

                

        }
    }
}

